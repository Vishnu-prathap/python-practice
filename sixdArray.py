def hourglassSum(arr):
    result = float('-inf')

    i,j=1,1

    for n in range(16):
        cur = 0

        cur = arr[i][j] + arr[i-1][j-1] + arr[i-1][j] + arr[i-1][j+1] + arr[i+1][j-1] + arr[i+1][j] + arr[i+1][j+1]

        result = max(result, cur)
 
        j+=1

        if j == 5:
            i += 1
            j = 1
    return result

